/**
 * Valid values for the CSS property 'white-space': https://developer.mozilla.org/en-US/docs/Web/CSS/white-space#syntax
 */
enum WhiteSpaceValues {
  NORMAL = 'normal',
  NOWRAP = 'nowrap',
  PRE = 'pre',
  PRE_WRAP = 'pre-wrap',
  PRE_LINE = 'pre-line',
  BREAK_SPACES = 'break-spaces',
}

export default WhiteSpaceValues;
